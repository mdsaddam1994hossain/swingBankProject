    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String custid = jTextField1.getText();
       
       
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/banking_system",
                    "j2ee","1234");
          PreparedStatement   pstmt = con.prepareStatement("select first_name,last_name,street,city,phone from customer where cust_id =?");
             pstmt.setString(1, custid);
           ResultSet  rs = pstmt.executeQuery();
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, "Customer Number not found");
            }else{
            
                String fname= rs.getString(1);
                String lname= rs.getString(2);
                String street= rs.getString(3);
                String city= rs.getString(4);
                String phone= rs.getString(5); 
                
                
                jTextField2.setText(fname.trim());
                jTextField3.setText(lname.trim());
                jTextField4.setText(street.trim());
                jTextField5.setText(city.trim());
                jTextField6.setText(phone.trim());
            
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerUpdate.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
          
            
    }//GEN-LAST:event_jButton1ActionPerformed

