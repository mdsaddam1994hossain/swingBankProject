    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String custid= jTextField1.getText();
        String fname= jTextField2.getText();
        String lname= jTextField3.getText();
        String street= jTextField4.getText();
        String city= jTextField5.getText();
        String phone= jTextField6.getText();
        
        try {
            PreparedStatement pst= jdbc.getConnection().prepareStatement("update customer set first_name=?,last_name=?,street=?,city=?,phone=? where cust_id =? ");
            
            pst.setString(1, fname);
            pst.setString(2, lname);
            pst.setString(3, street);
            pst.setString(4, city);
            pst.setString(5, phone);
            pst.setString(6, custid);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "update successfull");
        } catch (SQLException ex) {
            Logger.getLogger(CustomerUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


